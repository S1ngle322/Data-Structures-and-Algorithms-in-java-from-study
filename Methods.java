package com.company;
public interface Methods<T> {

    /**
     * Check if the list is empty.
     * @return this.size == 0;
     */
    boolean isEmpty();

    /**
     * Add element e to the start of the list.
     * @param data
     */
    void addFirst(T data);

    /**
     * Add element e to the end of the list.
     * @param data
     */
    void addLast(T data);

    /**
     * Add element e at position index.
     * @param index
     * @param data
     */
    void add(int index, T data);

    /**
     * Delete element at position index.
     * @param index
     */
    void delete(int index);

    /**
     * Remove the first element from the list.
     */
    void deleteFirst();

    /**
     * Remove the last element from the list.
     */
    void deleteLast();

    /**
     * Delete element e if it exists in the list.
     * @param data
     */
    void deleteElement(T data);

    /**
     * Retrieve element at position index.
     * @param index
     * @return
     */
    T get(int index);

    /**
     * Replace element at position index with new element e.
     * @param index
     * @param data
     */
    void set(int index, T data);

    /**
     * @return size
     */
    int size();

    /**
     * Sort an array.
     */
    void selectionSort();

    /**
     * Print function for data.
     */
    void print();
}
